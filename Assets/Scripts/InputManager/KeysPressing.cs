using UnityEngine;
using System.Collections;

public class KeysPressing : MonoBehaviour 
{

    // ======================================================================================================================================== //
    void Start () 
    {

    }
    // ======================================================================================================================================== //
    void Update () 
    {
        // Esc to quit
        if (Input.GetKey (KeyCode.Escape))
            Application.Quit ();
    }
    // ======================================================================================================================================== //
}